# The if Condition
sports = "basketball"

if sports == "basketball"
  puts "You like basketball"
end

# if else Condition
fruit = "apple"

if fruit == "apple"
    puts "I like apple"
else
    puts "I don't like apple"
end

# Multiple Conditions

score = 65

if score > 60 and score < 80
  puts "You got B grade"
end

#Using elsif
score = 79

if score > 89
    puts "you got A grade"
elsif score > 75
    puts "you got B grade"
